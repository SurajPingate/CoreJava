/**
 *  28.	Initialize one String type of array and print the elements using for each loop.
 */

 class StringArray{
     public static void main(String[] args) {
         char arr[] = new char[]{'S','u','r','a','j','P','i','n','g','a','t','e'};

         for(char a : arr){
             System.out.print(" "+a);
         }
     }
 }